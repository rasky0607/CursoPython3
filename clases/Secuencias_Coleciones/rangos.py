#Los rangos, esto entra dentro del tipo secuencia/coleccion y son INMUTABLES
#Podemos crear rangos de 3 formas usando su constructor:
#Ej1:mirango=range(10) -> crea valores del 0 al 9
#Ej2:mirango=range(10,20)-> crea valores del 10 al 19
#Ej3:mirango=range(10,20,2) crea valores del 10 al 19 de dos en dos
#Podemos acceder a atributos del objeto range:
	#start-> mirango.start Nos india el valor por el que comienza
	#stop-> mirango.stop Nos idnica el final del rango
	#step-> mirango.step Nos indica el intervalo que le indicamos

